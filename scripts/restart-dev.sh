#!/bin/bash

# Цвета для вывода
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
RED='\033[0;31m'
BLUE='\033[0;34m'
NC='\033[0m' # No Color

echo -e "${BLUE}Перезапуск сервера разработки...${NC}"

# Запускаем скрипт очистки портов
./scripts/kill-ports.sh

# Проверяем, что все порты освободились
sleep 1

# Запускаем сервер разработки в фоновом режиме
echo -e "${GREEN}Запускаем сервер разработки...${NC}"
PORT=3000 next dev &

# Сохраняем PID процесса
DEV_PID=$!
echo -e "${GREEN}Сервер разработки запущен с PID: $DEV_PID${NC}"
echo -e "${YELLOW}Для остановки сервера выполните: kill -9 $DEV_PID${NC}"

# Ждем несколько секунд, чтобы убедиться, что сервер запустился
sleep 3

# Проверяем, что процесс все еще работает
if ps -p $DEV_PID > /dev/null; then
  echo -e "${GREEN}Сервер работает на http://localhost:3000${NC}"
  echo -e "${YELLOW}Логи сервера будут выводиться ниже:${NC}"
  
  # Ждем завершения процесса
  wait $DEV_PID
else
  echo -e "${RED}Не удалось запустить сервер разработки.${NC}"
  echo -e "${YELLOW}Проверьте, не занят ли порт 3000:${NC}"
  lsof -i :3000
fi 